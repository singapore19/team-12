def find_mrt(x, y):
    """
    returns nearest mrt(string) given lat, lon coordinates
    by some mtd w a fancy-ass name, "voronoi diagrams"
    """
    wd = (x - 1.375845) ** 2 + (y - 103.776289) ** 2
    gl = (x - 1.314834) ** 2 + (y - 103.891121) ** 2
    bv = (x - 1.304584) ** 2 + (y - 103.796945) ** 2
    db = (x - 1.307006) ** 2 + (y - 103.843568) ** 2
    cg = (x - 1.339967) ** 2 + (y - 103.731715) ** 2

    l = (wd, gl, bv, db, cg)
    X = l.index(min(l))
    place = "nil"
    mini = 99999999
    print(X)
    nearest_centroid = centroids_list[X]

    for k, val in nearest_centroid.items():
        dist = (x - float(val[0])) ** 2 + (y - float(val[1])) ** 2
        if (dist < mini):
            place = k
            mini = dist

    return place


